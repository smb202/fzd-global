// Apply translations to DOM and persist language choice
window.FZD_setLang = function(lang) {
  if (!['en','de'].includes(lang)) lang = 'en';
  try { localStorage.setItem('fzd_lang', lang); } catch(e){}
  document.documentElement.lang = lang;
  const dict = window.FZD_I18N[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });
  // toggle button states
  document.querySelectorAll('[data-lang-btn]').forEach(b => {
    b.classList.toggle('is-active', b.getAttribute('data-lang-btn') === lang);
  });
  // dispatch event so pages can react
  window.dispatchEvent(new CustomEvent('fzd:langchange', { detail: { lang }}));
};

window.FZD_getLang = function() {
  try { return localStorage.getItem('fzd_lang') || 'en'; } catch(e) { return 'en'; }
};

document.addEventListener('DOMContentLoaded', () => {
  window.FZD_setLang(window.FZD_getLang());
});
