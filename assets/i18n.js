// Apply translations to DOM and persist language choice
window.FZD_setLang = function(lang) {
  if (!['en','de'].includes(lang)) lang = 'en';
  try { localStorage.setItem('fzd_lang', lang); } catch(e){}

  // Navigate between language versions instead of translating in place
  const path = window.location.pathname;
  const hash = window.location.hash;
  const isInDe = /\/de\//.test(path);

  if (lang === 'de' && !isInDe) {
    // /some/page.html → /some/de/page.html
    const dePath = path.replace(/\/([^/]+\.html)$/, '/de/$1');
    window.location.href = dePath + hash;
    return;
  }
  if (lang === 'en' && isInDe) {
    // /some/de/page.html → /some/page.html
    const enPath = path.replace('/de/', '/');
    window.location.href = enPath + hash;
    return;
  }
  document.documentElement.lang = lang;
  const dict = window.FZD_I18N[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });
  // update hrefs for language-specific pages
  document.querySelectorAll('[data-href-de]').forEach(el => {
    el.setAttribute('href', lang === 'de' ? el.getAttribute('data-href-de') : el.getAttribute('data-href-en'));
  });
  // toggle button states
  document.querySelectorAll('[data-lang-btn]').forEach(b => {
    b.classList.toggle('is-active', b.getAttribute('data-lang-btn') === lang);
  });
  // dispatch event so pages can react
  window.dispatchEvent(new CustomEvent('fzd:langchange', { detail: { lang }}));
};

window.FZD_getLang = function() {
  if (window.FZD_FORCE_LANG) return window.FZD_FORCE_LANG;
  try { return localStorage.getItem('fzd_lang') || 'en'; } catch(e) { return 'en'; }
};

document.addEventListener('DOMContentLoaded', () => {
  window.FZD_setLang(window.FZD_getLang());
});
